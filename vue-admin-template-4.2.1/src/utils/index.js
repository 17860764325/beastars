export function debounce(func, wait, immediate) {
  let timeout, args, context, timestamp, result

  const later = () => {
    // 据上一次触发时间间隔
    const last = +new Date() - timestamp

    // 上次被包装函数被调用时间间隔last小于设定时间间隔wait
    if (last < wait && last > 0) {
      timeout = setTimeout(later, wait - last)
    } else {
      timeout = null
      // 如果设定为immediate===true，因为开始边界已经调用过了此处无需调用
      if (!immediate) {
        result = func.apply(context, args)
        if (!timeout) context = args = null
      }
    }
  }

  return (...args) => {
    context = this
    timestamp = +new Date()
    const callNow = immediate && !timeout
    // 如果延时不存在，重新设定延时
    if (!timeout) timeout = setTimeout(later, wait)
    if (callNow) {
      result = func.apply(context, args)
      context = args = null
    }

    return result
  }
}

export function deepClone(source) {
  if (!source && typeof source !== 'object') {
    throw new Error('error arguments', 'shallowClone')
  }
  const targetObj = source.constructor === Array ? [] : {}
  for (const keys in source) {
    if (source.hasOwnProperty(keys)) {
      if (source[keys] && typeof source[keys] === 'object') {
        targetObj[keys] = source[keys].constructor === Array ? [] : {}
        targetObj[keys] = deepClone(source[keys])
      } else {
        targetObj[keys] = source[keys]
      }
    }
  }
  return targetObj
}

export function objPropTrim(obj) {
  if (obj) {
    if (Array.isArray(obj)) {
      obj = obj.map(item => objPropTrim(item))
    } else {
      if (typeof obj === 'string') {
        return obj.trim()
      } else if (typeof obj === 'object') {
        Object.keys(obj).forEach(key => {
          obj[key] = objPropTrim(obj[key])
        })
      }
    }
  }
  return obj
}

export function randomString(len) {
  len = len || 32
  const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789'
  const maxPos = chars.length
  let pwd = ''
  for (let i = 0; i < len; i++) {
    pwd += chars.charAt(Math.floor(Math.random() * maxPos))
  }
  return pwd
}

/**
 * 列表数据重复检查
 * @param {Array} data 列表数据
 * @param {String|Array} key 被检查字段
 * @returns {Object} { result, duplicateRows }
 */
export function checkDuplicatedData(data, key) {
  if (!data || !data.length) {
    console.error('源数据为空')
    return
  }
  if (!key) {
    console.error('未设置被检测字段')
    return
  }
  const map = {}
  if (data instanceof Array) {
    if (key instanceof Array) {
      data.forEach((el, i) => {
        const value = key.map(e => el[e])
        if (map[value]) {
          map[value].push(i)
        } else {
          map[value] = [i]
        }
      })
    }
    if (typeof key === 'string') {
      data.forEach((el, i) => {
        if (map[el[key]]) {
          map[el[key]].push(i)
        } else {
          map[el[key]] = [i]
        }
      })
    }
  } else {
    console.error('源数据类型错误')
  }
  let duplicateRows = []
  Object.keys(map).forEach(el => {
    if (map[el].length > 1) {
      duplicateRows = [...duplicateRows, ...map[el]]
    }
  })
  duplicateRows = duplicateRows.map(el => el + 1).sort((a, b) => a - b)
  if (duplicateRows.length) {
    return {
      result: true,
      duplicateRows
    }
  } else {
    return {
      result: false
    }
  }
}

export function getUrlParam(name) {
  const reg = new RegExp('(^|&)' + name + '=([^&]*)(&|$)', 'i')
  const r = window.location.search.substr(1).match(reg)
  if (r !== null) {
    return decodeURI(r[2])
  } else {
    return null
  }
}

export function capitalization(n) {
  if (!/^-?(0|[1-9]\d*)(\.\d+)?$/.test(n)) {
    return ''
  }
  const fraction = ['角', '分']
  const digit = ['零', '壹', '贰', '叁', '肆', '伍', '陆', '柒', '捌', '玖']
  const unit = [
    ['元', '万', '亿'],
    ['', '拾', '佰', '仟']
  ]
  const head = n < 0 ? '欠' : ''
  n = Math.abs(n)
  let s = ''
  for (let i = 0; i < fraction.length; i++) {
    s += (digit[Math.floor(n * 10 * Math.pow(10, i)) % 10] + fraction[i]).replace(/零./, '')
  }
  s = s || '整'
  n = Math.floor(n)
  for (let i = 0; i < unit[0].length && n > 0; i++) {
    let p = ''
    for (let j = 0; j < unit[1].length && n > 0; j++) {
      p = digit[n % 10] + unit[1][j] + p
      n = Math.floor(n / 10)
    }
    s = p.replace(/(零.)*零$/, '').replace(/^$/, '零') + unit[0][i] + s
  }
  return (
    head +
    s
      .replace(/(零.)*零元/, '元')
      .replace(/(零.)+/g, '零')
      .replace(/^整$/, '零元整')
  )
}

export function callHook(vm, context, hook, hookType) {
  if (!context) {
    console.warn('context is undefined')
    return
  }
  let result
  const handler = context[hook]
  if (handler) {
    result = handler.call(vm, hookType)
  } else {
    console.warn(`hook【${hook}】未定义`)
  }
  return result
}
